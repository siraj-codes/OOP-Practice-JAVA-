class Bitwise {
	public static void main (String args[]) {

		byte a= 1 , b= 20;
		
		
		
		System.out.println("\t"+(a|b));

		System.out.println("\t"+(a^b));
		
		System.out.println("\t"+(a&b));

		System.out.println("\t"+(~a));

		System.out.println("\t"+(~b));

		System.out.println("\t"+(~a|b));

		System.out.println("\t"+(a|~b));

		System.out.println("\t"+(~a|~b));

		
	} 
}